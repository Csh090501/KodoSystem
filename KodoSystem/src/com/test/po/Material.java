package com.test.po;

public class Material {
	private Integer mat_no; // 原材料编号
	private String mat_id; // 原材料id
	private String mat_name; // 原材料名称
	private Integer mat_number; // 原材料的数量

	public Integer getMat_no() {
		return mat_no;
	}

	public void setMat_no(Integer mat_no) {
		this.mat_no = mat_no;
	}

	public String getMat_id() {
		return mat_id;
	}

	public void setMat_id(String mat_id) {
		this.mat_id = mat_id;
	}

	public String getMat_name() {
		return mat_name;
	}

	public void setMat_name(String mat_name) {
		this.mat_name = mat_name;
	}

	public Integer getMat_number() {
		return mat_number;
	}

	public void setMat_number(Integer mat_number) {
		this.mat_number = mat_number;
	}

}
